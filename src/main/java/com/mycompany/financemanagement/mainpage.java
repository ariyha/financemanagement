    public void transactionno() throws IOException{
        ReversedLinesFileReader fr = new ReversedLinesFileReader(new File("users//"+mailid+"transactions.csv"));
        String line2;
        
        while((line2=fr.readLine())!=null){
            nooftrans++;
        }

    }
    
    
   public void buildtable() throws IOException{
        ReversedLinesFileReader fr = new ReversedLinesFileReader(new File("users//"+mailid+"transactions.csv"));
        String line2;

       table = new String[nooftrans][6];
       for(int i=0;i<nooftrans;i++){
           line2 = fr.readLine();
           for(int j=0;j<6;j++){
               table[i][j]=line2.split(",")[j];
           }
       }
   }

