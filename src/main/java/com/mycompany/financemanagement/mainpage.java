    public void getbalance() throws FileNotFoundException, IOException{
        String[] arrOfStr = line.split(",");
        mailid = arrOfStr[1];
        FileReader reader = new FileReader("users//"+mailid+"transactions.csv");
        BufferedReader bufread;
        bufread = new BufferedReader(reader);
        bufread.readLine();
        String line2;
        while((line2 = bufread.readLine())!=null){
            balance = Integer.parseInt(line2.split(",")[4]);
        }

    }
